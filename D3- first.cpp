#include <iostream>
using namespace std;
int main ()
{
    int x,y;
    cout<<"Please enter 2 numbers " <<endl;
    cin>>x>>y;

    cout<<"x+y= "<<(x+y) <<endl;
    cout<<"x-y= "<<(x-y) <<endl;
    cout<<"x*y= "<<(x*y) <<endl;
    cout<<"x/y= "<<(x/y) <<endl;
    cout<<"x%y= "<<(x%y) <<endl;
    cout<<"x++= "<<(x++) <<endl;
    cout<<"++x= "<<(++x) <<endl;
    cout<<"y++= "<<(y++) <<endl;
    cout<<"++y= "<<(++y) <<endl;

    return 0;
}
